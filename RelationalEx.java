public class RelationalEx
{
  public static void main(String args [])
    { 
       int n1=30;
       int n2=20;
       System.out.println(n1);
       System.out.println(n1==n2);
       System.out.println(n1>n2);
       System.out.println(n1<n2);
       System.out.println(n1<=n2);
       System.out.println(n1>=n2);
       System.out.println(n1!=n2);
}
}
       


       

       

       
     